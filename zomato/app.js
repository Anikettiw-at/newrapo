function getres(restaurants){
    const root = document.getElementById('root');

    restaurants.forEach(restaurant => {
        const card = document.createElement('div');
card.classList.add('card');//class namediye
 
const image = document.createElement("img");
image.src=`Images/${restaurants.image}.jpg`;
//card content
const cardcontent = document.createElement('div');
cardcontent.classList.add('cardcontent');

//card header

const card_header = document.createElement('div');
card_header.classList.add('cardheader')
// ab card header ke andardo div aur 

const h3 = document.createElement('h3');
h3.textContent = restaurants.name;

const rate= document.createElement('span');
rate.textContent = restaurants.rating;
rate.classlist.add('rating');

card_header.appendChild(h3);
card_header.appendChild(rate);


//card footer
const card_footer = document.createElement('div');
card_footer.classList.add('cardfooter')

const food = document.createElement('span');
food.textContent = restaurants.food_type;

const price = document.createElement('span');
price .textContent = restaurants.price;


card_footer.appendChild(food);
card_footer.appendChild(price);


//card location
const card_location = document.createElement('div');
card_location.classList.add('cardlocation');

const location = document.createElement('span');
location.textContent = restaurants.location;

const distance = document.createElement('span');
distance.textContent = restaurants.distance;


card_location.appendChild(location);
card_location.appendChild(distance);

//ab headr footer location  ko card content me daalo 

cardcontent.appendChild(card_header);
cardcontent.appendChild(card_footer);
cardcontent.appendChild(card_location);

// ab card ke andarcontent aur img daalo 

card.appendChild(image);
card.appendChild(cardcontent); 

root.appendChild(card);



    });
}

getres(restaurants);







